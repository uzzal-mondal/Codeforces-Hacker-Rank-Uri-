package Java.Array;

public class SimpleArray {
    public static void main(String args[]) {
        String name[] = new String[3];
        name[0] = "uzzal";
        name[1] = "suzal";
        name[2] = "muzahid";

        for (int i = 0; i<name.length; i++){
            System.out.println(name[i]);
        }
    }
}
